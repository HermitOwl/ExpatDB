    private void addSaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addSaveButtonActionPerformed
        // TODO add your handling code here:
        selectedPerson = new Person();
        selectedPerson.setTitle(titleTextField.getText().trim());
        selectedPerson.setFirstName(firstNameField.getText().trim());
        selectedPerson.setMiddleName(middleNameField.getText().trim());
        jProgressBar1.setValue(20);
        selectedPerson.setLastName(lastNameField.getText().trim());
        selectedPerson.setProfession(professionField.getText().trim());
        if (dobField.getText().equals("YYYY-MM-DD")) {
            selectedPerson.setDate("");
        } else {
            selectedPerson.setDate(dobField.getText().trim());
        }
        selectedPerson.setGender(maleRadioButton.isSelected());
        selectedPerson.setMaritalStatus(maritalStatusCombo.getSelectedIndex());
        jProgressBar1.setValue(60);
        //Including Address Information
        String houseName, streetName, postCode, townCity;
        if (houseNumberField.getText().trim().equals("House/ Flat Number")) {
            houseName = "";
        } else {
            houseName = houseNumberField.getText().trim();
        }
        if (streetNameField.getText().trim().equals("Street Name")) {
            streetName = "";
        } else {
            streetName = streetNameField.getText().trim();
        }
        if (postCodeField.getText().trim().equals("Post Code")) {
            postCode = "";
        } else {
            postCode = postCodeField.getText().trim();
        }
        if (townCityField.getText().trim().equals("Town/City")) {
            townCity = "";
        } else {
            townCity = townCityField.getText().trim();
        }
        selectedPerson.setAddress(new Address(houseName, streetName, postCode, townCity));
        //setting up phoneNumber;
        String telNumber;
        if (phoneNumberField.getText().trim().equals("Phone Number")) {
            telNumber = "";
        } else {
            telNumber = phoneNumberField.getText().trim();
        }
        selectedPerson.setTelephone(new Telephone(telNumber, phoneNumberTypeCombo.getSelectedIndex()));
        jProgressBar1.setValue(80);
        //Setting up passport
        String doe;
        String doi;
        if (doIssueField.getText().trim().equals("YYYY-MM-DD")) {
            doi = "";
        } else {
            doi = doIssueField.getText().trim();
        }
        if (doExpiryField.getText().trim().equals("YYYY-MM-DD")) {
            doe = "";
        } else {
            doe = doExpiryField.getText().trim();
        }
        selectedPerson.setPassport(new Passport(passportNumberField.getText().trim(), placeOfIssueField.getText().trim(), doi, doe));
        jProgressBar1.setValue(0);
        this.clearAddForms();// reset for next addition

        if (!persons.contains(selectedPerson)) {
            persons.add(selectedPerson);
        }
        selectedPerson = new Person();
        this.populateSubjectList();
       

    }//GEN-LAST:event_addSaveButtonActionPerformed

