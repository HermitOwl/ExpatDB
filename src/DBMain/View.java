    private void subjectListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_subjectListValueChanged
        try {
            int[] selectedIndices = subjectList.getSelectedIndices();
            if (!persons.isEmpty()) {
                selectedPerson = persons.get(subjectList.getSelectedIndex());
            }
            String details = "";
            for (int i = 0; i < selectedIndices.length; i++) {
                details = details + persons.get(selectedIndices[i]).toString();
                details = details + "*****************************************************\n";
            }
            entryViewArea.setText(details);
            editTitleTextField.setText(selectedPerson.getTitle());
            editFirstNameField1.setText(selectedPerson.getFirstName());
            editMidNameField1.setText(selectedPerson.getMiddleName());
            editLastNameField1.setText(selectedPerson.getLastName());
            if (selectedPerson.getLastName().equals("")) {
                editDOBField1.setText("");
            } else {
                editDOBField1.setText("" + LocalDate.parse(selectedPerson.getDate()));
            }
            if (selectedPerson.getRawGender()) {
                editMaleRadioButton1.setSelected(true);
            } else {
                editFemaleRadioButton1.setSelected(true);
            }
            maritalStatusCombo1.setSelectedIndex(selectedPerson.getMaritalStatus());
            houseNumberField1.setText(selectedPerson.getAddress().getHouseName());
            streetNameField1.setText(selectedPerson.getAddress().getStreetName());
            townCityField1.setText(selectedPerson.getAddress().getTownCity());
            postCodeField1.setText(selectedPerson.getAddress().getPostCode());
            phoneNumberField1.setText(selectedPerson.getTelephone().getPhoneNumber());
            phoneNumberTypeCombo1.setSelectedIndex(selectedPerson.getTelephone().getRawPhoneNumberType());

            passportNumberField1.setText(selectedPerson.getPassport().getPassportNumber());
            editPlaceOfIssueField.setText(selectedPerson.getPassport().getPlaceOfIssue());
            editProfessionField.setText(selectedPerson.getProfession());
            if (selectedPerson.getPassport().getDateOfIssue().equals("")) {
                editDoIssueField.setText("");
            } else {

                editDoIssueField.setText("" + LocalDate.parse(selectedPerson.getPassport().getDateOfIssue()));
            }

            if (selectedPerson.getPassport().getDateOfExpiry().equals("")) {
                editDoExpiryField.setText("");
            } else {

                editDoExpiryField.setText("" + LocalDate.parse(selectedPerson.getPassport().getDateOfExpiry()));
            }

            jTabbedPane1.setSelectedIndex(0);
        } catch (java.time.format.DateTimeParseException | ArrayIndexOutOfBoundsException e) {

        }
    }//GEN-LAST:event_subjectListValueChanged

